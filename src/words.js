/**
 * @fileoverview Word Database
 * @file src/words.js
 * @description Contains the complete list of words that can be used in the game.
 *              Provides the word pool from which random words are selected.
 * 
 * This file exports a comprehensive array of common English words that players
 * must guess during gameplay. The word list serves as the game's vocabulary
 * database and determines the difficulty and variety of words encountered.
 * 
 * **Word Format:**
 * - All words are stored in lowercase format
 * - Words are converted to uppercase when selected for gameplay
 * - Contains approximately 480+ common English words
 * - Includes nouns, verbs, adjectives, and other frequently used words
 * 
 * **Word Requirements:**
 * - Must contain only letters (A-Z), no numbers or special characters
 * - Must not contain spaces or hyphens
 * - Should be common English words for appropriate difficulty
 * - Words are case-insensitive during selection (stored lowercase, played uppercase)
 * 
 * **Usage:**
 * Words are randomly selected from this array using the getRandomWord() function
 * in utils.js. Each game round uses one word from this list, and words can be
 * repeated across different game sessions (no tracking of used words).
 * 
 * **Modification Guidelines:**
 * - To add words: Append new lowercase string entries to the array
 * - To remove words: Delete unwanted entries from the array
 * - To change difficulty: Add/remove words of appropriate complexity
 * - Maintain alphabetical organization (optional, for easier maintenance)
 * 
 * **Word Selection:**
 * The selection algorithm ensures each word has an equal probability of being
 * chosen. There is no mechanism to prevent words from repeating across games,
 * which adds to the replayability and unpredictability of the game.
 * 
 * @see {@link ./utils.js} for getRandomWord() function that uses this array
 */

